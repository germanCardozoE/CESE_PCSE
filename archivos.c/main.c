/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t j;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_TIM14_Init();
  MX_TIM3_Init();
  MX_TIM16_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_IC_Start_IT(&htim14, TIM_CHANNEL_1);
	
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1); // config tim3 as pwm
	
	HAL_UART_Receive_IT(&huart1,uartRxBuffer.uartMsg,1); //enable Uart Rx interrupt
	
	__HAL_UART_ENABLE_IT(&huart1, UART_IT_TC);   //enable Uart Tx interrupt 
	__HAL_UART_ENABLE_IT(&huart1, UART_IT_RXNE); //enable Uart Rx interrupt
	
	//sendMsg(&huart1,uartTxBuffer); // print start string
	
	HAL_UART_Transmit_IT(&huart1,uartTxBuffer,strlen((char*)uartTxBuffer));
	
	HAL_TIM_OC_Start_IT(&htim16, TIM_CHANNEL_1);
			
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	
  while (1)
  {
		///// ZERO DETECTION /////
		if(getZeroInterruptFlag()==TRUE)
		{
			resetOCCouenter(&htim16); //OC start
			Zero_Interrupt(&htim14);
			clrZeroFlag();
			
			frec=getACFrecuency();
					__asm("nop");
		}
		frec++;
		frec--;
		__asm("nop");
		
		///// UART CONTROL //////////
		// lectura del flag de interrupcion por uart
		if(getUartFlag()==TRUE) // si se recibio algun dato BT
		{
			clrUartFlag(); //limpio el flag
			UART_Rx_Interrupt(&huart1,&uartRxBuffer); //ejecuto interrupcion

			if(uartRxBuffer.uartFlag==COMPLETE) // evalua si termino de recibir datos
			{
				uartRxBuffer.uartFlag=INCOMPLETE;
				
				if(validateMsj(uartRxBuffer.uartMsg)) //pregunta si es una orden valida
				{
					switch(uartRxBuffer.uartMsg[uartRxBuffer.uartLength-1]) //la orden esta en el ultimo caracter valido. 
					{
						case '1':
										strcpy((char *)uartTxBuffer,"OK 1");
										if(pwm>0)
											pwm--;
										duttyChange(&htim3,pwm); // PWM control
						break;	
						case '2':
										strcpy((char *)uartTxBuffer,"OK 2");
										if(pwm<PWMMAX)
											pwm++;
										duttyChange(&htim3,pwm);
						break;	
						case '3':
										strcpy((char *)uartTxBuffer,"OK 3");
										if(shoot>0)
											shoot--;		
										shootChange(&htim16,shoot);									
											
						break;	
						case '4':
										strcpy((char *)uartTxBuffer,"OK 4");
										if(shoot<SHOOTMAX)
											shoot++;
										shootChange(&htim16,shoot);
						break;
						case '5':
										strcpy((char *)uartTxBuffer,"OK 5");
										shoot=0;
										pwm=0;
										duttyChange(&htim3,pwm);
										shootChange(&htim16,shoot);	
						break;	
						case '6':
										strcpy((char *)uartTxBuffer,"OK 6");
										shoot=SHOOTMAX;
										pwm=PWMMAX;
										duttyChange(&htim3,pwm);
										shootChange(&htim16,shoot);
						break;										
						default:
										strcpy((char *)uartTxBuffer,"NULL");
						break;
					}
					
					
				
				HAL_UART_Transmit_IT(&huart1,uartTxBuffer,strlen((char*)uartTxBuffer));
					
				}					
			}
		}
		else
		{
			//do nothing
			j=0;
			j++;
		}
	}		
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  
  /* USER CODE END 3 */
}

